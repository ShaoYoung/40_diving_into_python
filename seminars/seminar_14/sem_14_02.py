def clean_text(text: str) -> str:
    """
    Delete symbols from text other than A(a) - Z(z) and space. Return string in lower register.
    >>> clean_text('python')
    'python'
    >>> clean_text('Python')
    'python'
    >>> clean_text('python, but not java')
    'python but not java'
    >>> clean_text('python - —ç—Ç–æ –Ω–µ –ø–∏—Ç–æ–Ω')
    'python    '
    >>> clean_text('"Python" - —ç—Ç–æ –Ω–µ "–ü–∏—Ç–æ–Ω"')
    'python    '
    """
    text = text.lower()
    av_symbols = [chr(code) for code in range(97, 123)]
    av_symbols.append(' ')
    for symbol in text:
        if symbol not in av_symbols:
            text = text.replace(symbol, '')
    return text


